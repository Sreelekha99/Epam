public class calculator {
	/**
	 * method to add two numbers
	 * @param x is first number
	 * @param y is second number
	 */
	public int Add(int x,int y) {
		return x+y;
	}
	/**
	 * method to add three numbers
	 * @param x is first number
	 * @param y is second number
	 * @param z is third number
	 */
	public int Add(int x,int y,int z) {
		return x+y+z;
	}
	/**
	 * method to subtract two numbers
	 * @param x is first number
	 * @param y is second number
	 */
	public int Sub(int x,int y) {
		return x-y;
	}
	/**
	 * method to multiply two numbers
	 * @param x is first number
	 * @param y is second number
	 */
	public int Mul(int x,int y) {
		return x*y;
	}
	/**
	 * method to multiply three numbers
	 * @param a is first number
	 * @param b is second number
	 * @param c is third number 
	 */
	public int Mul(int x,int y,int z) {
		return x*y*z;
	}
	
	/**
	 * method for division of two numbers
	 * @param a is first number
	 * @param b is second number
	 */
	public int Div(int x,int y) {
		return x/y;
	}
}
